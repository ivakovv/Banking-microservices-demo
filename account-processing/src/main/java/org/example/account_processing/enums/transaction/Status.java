package org.example.account_processing.enums.transaction;


/**
 * Статусы банковской транзакции.
 * Определяет текущее состояние и жизненный цикл финансовой операции.
 */
public enum Status {

    /**
     * Транзакция разрешена к выполнению.
     * Прошла первоначальные проверки и ожидает обработки.
     * Может быть отменена или заблокирована на следующих этапах.
     */
    ALLOWED,

    /**
     * Транзакция находится в процессе обработки.
     * Выполняются финансовые операции, проверки безопасности
     * и взаимодействие с платежными системами.
     */
    PROCESSING,

    /**
     * Транзакция успешно завершена.
     * Средства списаны/зачислены, все операции выполнены.
     * Финальный статус успешной транзакции.
     */
    COMPLETE,

    /**
     * Транзакция заблокирована системой безопасности.
     * Блокировка может быть вызвана подозрением в мошенничестве,
     * превышением лимитов или техническими причинами.
     */
    BLOCKED,

    /**
     * Транзакция отменена клиентом или системой.
     */
    CANCELLED
}

