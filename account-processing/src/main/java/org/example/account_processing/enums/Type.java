package org.example.account_processing.enums;

/**
 * Типы банковских операций по счетам и картам.
 * Определяет направление движения денежных средств в транзакциях.
 */
public enum Type {

    /**
     * Операция зачисления средств на счет.
     * Увеличивает баланс счета получателя.
     */
    DEPOSIT,

    /**
     * Операция списания средств со счета.
     * Уменьшает баланс счета отправителя.
     */
    WITHDRAW
}