package org.example.account_processing.enums;

/**
 * Статусы банковского счета и карты.
 * Определяет текущее состояние и доступность финансового инструмента для операций.
 */
public enum Status {

    /**
     * Счет/карта открыты, но еще не активированы.
     * Требуется активация для начала использования.
     */
    OPENED,

    /**
     * Счет/карта активны и доступны для использования.
     * Разрешены все операции в соответствии с тарифом и лимитами.
     */
    ACTIVE,

    /**
     * Счет/карта закрыты клиентом или банком.
     * Операции полностью невозможны.
     */
    CLOSED,

    /**
     * Счет/карта временно заморожены.
     * Операции временно приостановлены.
     */
    FROZEN,

    /**
     * Счет/карта арестованы по решению суда или правоохранительных органов.
     * Арест накладывается в рамках исполнительного производства.
     * Любые операции полностью заблокированы.
     */
    ARRESTED
}
