package org.example.client_processing.enums.client_product;

/**
 * Статусы банковского продукта клиента.
 * Определяет текущее состояние продукта, присвоенного клиенту.
 */
public enum Status {

    /**
     * Продукт активен и доступен для использования.
     * Клиент может совершать операции с продуктом в соответствии с его типом.
     */
    ACTIVE,

    /**
     * Продукт закрыт клиентом или банком.
     * Закрытие может быть инициировано по заявлению клиента или
     * в соответствии с условиями договора.
     * После закрытия операции с продуктом невозможны.
     */
    CLOSED,

    /**
     * Продукт временно заблокирован.
     * Блокировка может быть наложена по инициативе банка при подозрении
     * на мошенничество, по требованию правоохранительных органов
     * или при технических проблемах.
     * Восстановление доступа требует разблокировки.
     */
    BLOCKED,

    /**
     * Продукт арестован по решению суда или правоохранительных органов.
     * Арест накладывается в рамках исполнительного производства
     * или следственных действий.
     * Операции с продуктом полностью запрещены до снятия ареста.
     */
    ARRESTED
}
