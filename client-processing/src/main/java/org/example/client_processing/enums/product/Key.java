package org.example.client_processing.enums.product;

/**
 * Типы банковских продуктов.
 * Классификация финансовых продуктов и услуг, предоставляемых банком.
 */
public enum Key {

    /**
     * Дебетовая карта (Debit Card)
     * Банковская карта для расчетов за счет собственных средств клиента
     */
    DC,

    /**
     * Кредитная карта (Credit Card)
     * Банковская карта с предоставлением кредитной линии
     */
    CC,

    /**
     * Автокредит (Auto Credit)
     * Целевой кредит на приобретение автомобиля
     */
    AC,

    /**
     * Ипотечный кредит (Mortgage)
     * Долгосрочный кредит под залог недвижимости
     */
    IPO,

    /**
     * Потребительский кредит (Personal Credit)
     * Нецелевой кредит на личные нужды
     */
    PC,

    /**
     * Пенсионная программа (Pension)
     * Накопительные и страховые пенсионные продукты
     */
    PENS,

    /**
     * Накопительный счет (Savings Account)
     * Счет с начислением процентов на остаток
     */
    NS,

    /**
     * Страхование (Insurance)
     * Страховые продукты и услуги
     */
    INS,

    /**
     * Брокерское обслуживание (Brokerage Services)
     * Услуги на финансовых рынках и инвестиции
     */
    BS
}
