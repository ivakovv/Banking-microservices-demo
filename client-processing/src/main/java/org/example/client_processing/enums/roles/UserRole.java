package org.example.client_processing.enums.roles;

/**
 * Роли пользователей в банковской системе.
 * Определяет уровень доступа и права пользователей для выполнения операций.
 */
public enum UserRole {

    /**
     * Мастер-администратор системы.
     * Имеет полные права на все операции, включая создание, редактирование и удаление продуктов.
     */
    MASTER,

    /**
     * Старший сотрудник банка.
     * Может редактировать и удалять продукты, но не может создавать новые.
     */
    GRAND_EMPLOYEE,

    /**
     * Активный клиент банка.
     * Может пользоваться банковскими продуктами и услугами.
     */
    CURRENT_CLIENT,

    /**
     * Заблокированный клиент.
     * Доступ к сервисам ограничен, возвращается ошибка 403.
     */
    BLOCKED_CLIENT
}
